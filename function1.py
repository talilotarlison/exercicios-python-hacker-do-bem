# função range() -> range(stop) - Refere-se aos números inteiros a serem gerados, começando com zero.

for i in range(3):
      print(i)

# range([start], stop, [step] – Este é o número inicial da sequência, o número final na sequência e a diferença entre cada número na sequência.

for i in range(3,6):
     print(i)

for i in range(4, 10, 2):
      print(i)